    @Override
    public void run() {
        int s = 0; // giây
        int m = 0; // phút
        int h = 0; // giờ

        while (true) {
            s += 1;
            if (s == 60) {
                m += 1;
                s = 0;
            }
            if (m == 60) {
                h += 1;
                m = 0;
                s = 0;
            }
            if (h == 24) {
                h = 0;
                m = 0;
                s = 0;
            }
            lblDongHo.setText(h + ":" + m + ":" + s);

            try {
                // ngắt giữa các vòng lặp 1s
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(DongHo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
