    // Đổ dữ liệu lên table
    public void hienThiLenBang(List<QuanAo> danhSach) {
        // reset dòng về 0
        defaultTable.setRowCount(0);
        // đổ ds lên bảng
        for (QuanAo ma : danhSach) {
            defaultTable.addRow(new Object[]{
                ma.getMa(),
                ma.getTen(),
                ma.getLoai() == 1 ? "Quan" : "Ao",
                ma.getSize(),
                ma.getGia(),
                ma.getTrangThai()
            });
        }
    }

    // hiển thị dữ liệu đối tượng lên form
    public void hienThiLenForm(QuanAo ma) {
        txtTen.setText(ma.getTen());
        txtMa.setText(String.valueOf(ma.getMa()));
        txtGia.setText(String.valueOf(ma.getGia()));
        cboSize.setSelectedItem(ma.getSize());
        if (ma.getLoai() == 1) {
            rdoQuan.setSelected(true);
        } else {
            rdoAo.setSelected(true);
        }
    }

    // lấy từ form về tạo ra đối tượng
    public QuanAo layDuLieuTuForm() {
        return new QuanAo(txtMa.getText(), txtTen.getText(), rdoQuan.isSelected() ? 1 : 2, cboSize.getSelectedItem().toString(), Double.parseDouble(txtGia.getText()));
    }

    public boolean kiemTra() {
        if (txtGia.getText().isEmpty() || txtMa.getText().isEmpty() || txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong");
            return false;
        }
        try {
            double gia = Double.parseDouble(txtGia.getText());
            if (gia < 0) {
                JOptionPane.showMessageDialog(this, "Gias phai lon hon 0");
                return false;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gia phai la so");
            return false;
        }

        return true;
    }

